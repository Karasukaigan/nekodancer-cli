import json
import os
import re
from datetime import date


def build_funscript(actions, inverted, license_type, title, range_val, duration, creator="NekoDancer", description="Generated by NekoDancer."):
    return {
        "inverted": inverted,
        "metadata": {
            "bookmarks": [],
            "chapters": [],
            "creator": creator,
            "description": description,
            "duration": int(duration),
            "license": license_type,
            "notes": "",
            "performers": [],
            "script_url": "",
            "tags": [],
            "title": title,
            "type": "basic",
            "video_url": ""
        },
        "range": range_val,
        "version": "1.0",
        "actions": actions,
    }


